export const gameName = (s: string) => {
  switch (s) {
    case "LuckBazar":
      return "Luck Bazar";
      break;

    case "Fatafat":
      return "Kolkata FF";
      break;

    case "SmartMatka":
      return "Smart Matka";
      break;

    case "OpenClose":
      return "Open Close";
      break;

    default:
      break;
  }
};
